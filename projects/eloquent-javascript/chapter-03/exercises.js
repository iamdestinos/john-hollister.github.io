////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//return smallest of two inputs
function min(number1, number2) {
  //determine if number1 is less than number2
  if (number1 <= number2) {
    //if true, return number1
    return number1;
  } else {
    //if false return number2
    return number2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//return boolean for whether input is even
function isEven(number) {
  //determine if input number is even
  if (number % 2 === 0) {
    //if true, return true
    return true;
  } else {
    //if false, return false
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//take an input string and char and return a number of how many times char appears in string
function countChars(string, char) {
  //var count is equal to 0
  var count = 0;
  //iterate through string
  for(let i = 0; i < string.length; i++) {
    //determine if current character in string is same as input char
    if(string[i] === char) {
      //if true then increment count
      count++;
    }
  }
  //return count
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//return number of B in string
function countBs(string) {
  //var count is equal to 0
  var count = 0;
  //iterate through input string
  for(let i = 0; i < string.length; i++) {
    //determine if current value in string is a B
    if(string[i] == 'B') {
      //if true increment count
      count++;
    }
  }
  //return count
  return count;
}

